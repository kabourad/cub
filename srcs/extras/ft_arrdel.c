#include "../../headers/cub.h"

void	ft_arrdel(char ***arr)
{
	int	i;

	i = 0;
	while ((*arr)[i])
	{
		ft_strdel(&(*arr)[i]);
		i++;
	}
	free(*arr);
	*arr = NULL;
}
